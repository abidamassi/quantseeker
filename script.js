// Add smooth reveal animation for sections
const revealSections = document.querySelectorAll('section');
const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
        }
    });
}, {
    threshold: 0.1,
    rootMargin: '0px 0px -100px 0px'
});

revealSections.forEach(section => {
    section.style.opacity = '0';
    section.style.transform = 'translateY(50px)';
    section.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
    revealObserver.observe(section);
});

// Add typing effect for hero title
function typeWriter(element, text, speed = 100) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    }
    
    type();
}

// Initialize typing effect when page loads
window.addEventListener('load', () => {
    const heroTitle = document.querySelector('.hero-title');
    if (heroTitle) {
        const originalText = heroTitle.textContent;
        setTimeout(() => {
            typeWriter(heroTitle, originalText, 50);
        }, 500);
    }
}); 

document.addEventListener("DOMContentLoaded", function() {
  const codeElement = document.getElementById("typing-code");
  if (!codeElement) return;

  const code = codeElement.innerText;
  codeElement.innerText = "";
  let i = 0;

  // Add blinking cursor
  const cursor = document.createElement('span');
  cursor.id = 'typing-cursor';
  cursor.style.display = 'inline-block';
  cursor.style.width = '1ch';
  cursor.style.color = '#fff';
  cursor.textContent = '|';
  codeElement.parentNode.appendChild(cursor);

  function type() {
    if (i < code.length) {
      codeElement.innerText += code.charAt(i);
      i++;
      setTimeout(type, 10);
    } else {
      // Remove cursor and re-highlight
      cursor.remove();
      if (window.Prism) Prism.highlightElement(codeElement);
    }
  }

  type();
}); 